package com.bean;

import java.util.Date;

public class Experience {
	private Integer study_id;
	private Integer userid;
	private Date study_startdate;
	private Date study_endtime;
	private String study_place;
	private String study_position;
	public Integer getStudy_id() {
		return study_id;
	}
	public void setStudy_id(Integer study_id) {
		this.study_id = study_id;
	}
	public Integer getUserid() {
		return userid;
	}
	public void setUserid(Integer userid) {
		this.userid = userid;
	}
	public Date getStudy_startdate() {
		return study_startdate;
	}
	public void setStudy_startdate(Date study_startdate) {
		this.study_startdate = study_startdate;
	}
	public Date getStudy_endtime() {
		return study_endtime;
	}
	public void setStudy_endtime(Date study_endtime) {
		this.study_endtime = study_endtime;
	}
	public String getStudy_place() {
		return study_place;
	}
	public void setStudy_place(String study_place) {
		this.study_place = study_place;
	}
	public String getStudy_position() {
		return study_position;
	}
	public void setStudy_position(String study_position) {
		this.study_position = study_position;
	}
	
	

}
